public class Allinonedt {
    public static void main(String[] args) {
        // All in one : Data Types
        boolean a = true;
        System.out.println("Boolean : " + a);
        char b = 'A';
        System.out.println("Char value : " + b);
        byte c = 121;
        System.out.println("Byte value : " + c);
        short d = 1111;
        System.out.println("Short value : " + d);
        int n = 4585;
        System.out.println("Number value : " + n);
        float f = 45.5f;
        System.out.println("Float value : " + f);
        double dd = 45.55d;
        System.out.println("Double value : " + dd);
        long ll = 455555555555l;
        System.out.println("Double value : " + ll);
    }
}
